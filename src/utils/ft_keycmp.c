#include "minishell.h"

int	ft_keycmp(const char *s1, const char *s2)
{
	size_t	i;

	i = 0;
	while ((s1[i] || s2[i]) && s1[i] != '=' && s2[i] != '=')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	if ((s1[i] == '=' && !s2[i]) || (s2[i] == '=' && !s1[i]))
		return (0);
	return (s1[i] - s2[i]);
}
